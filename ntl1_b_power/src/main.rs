fn pow(m: u64, n: u64, divisor: u64) -> u64 {
    if n == 0 {
        return 1;
    };
    let mut result = pow(m * m % divisor, n / 2, divisor);
    if n % 2 == 1 {
        result = result * m % divisor;
    };
    result
}

fn solve(m: u64, n: u64) {
    let large = 1_000_000_007_u64;
    println!("{}", pow(m, n, large));
}

fn main() {
    input! {
        m: u64,
        n: u64,
    }

    solve(m, n);
}
